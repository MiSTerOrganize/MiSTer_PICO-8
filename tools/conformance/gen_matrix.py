def main():
    for name, body in sorted(CARTS.items()):
        path = os.path.join(HERE, name + ".p8")
        with open(path, "w", encoding="ascii", newline="\n") as f:
            f.write(HEADER)
            f.write(body.lstrip("\n"))
        print(f"wrote {name}.p8")
    print(f"{len(CARTS)} matrix carts generated.")

if __name__ == "__main__":
    main()
