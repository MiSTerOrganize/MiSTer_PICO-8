#!/usr/bin/env python3
"""gen_matrix.py -- conformance-matrix cart generator (z8conformance Layer 2).

Emits m_*.p8 carts that sweep the PICO-8 API surface, each self-reporting
CONFVAL/CONFHASH lines exactly like the hand-written conformance carts:
the same cart runs on the local reference PICO-8 binary (goldens, LOCAL
only -- never committed/shipped) and on z8headless (zepto8, under test);
any line difference = a zepto8 conformance bug pinned to that API group.

Coverage philosophy: games can only misbehave through engine primitives,
so pinning the primitive surface protects carts nobody has played. Each
cart packs many parameter combinations into per-group screen hashes
(coarse but exhaustive) plus targeted CONFVALs (exact values, precise
diagnosis). Risky/lexer-dependent features are isolated in their own
carts so a load failure can't mask other groups.

Usage:  python3 gen_matrix.py          (writes m_*.p8 into this directory)
Then:   regenerate goldens (gen_goldens.ps1, local) + run run_conformance.sh.
"""
import os

HERE = os.path.dirname(os.path.abspath(__file__))

HEADER = """pico-8 cartridge // http://www.pico-8.com
version 42
__lua__
-- GENERATED by gen_matrix.py -- do not hand-edit (edit the generator).
-- conformance-matrix cart: emits CONFVAL/CONFHASH lines; identical output
-- on PICO-8 (-x) and z8headless = conformant.
function _rst()
 camera() clip() pal() palt() fillp(0) cls(0)
end
function _sh()
 local h=0
 for a=0x6000,0x7fff do h=bxor(rotl(h,3),@a) end
 return h
end
function _v(l,v)
 if type(v)=="number" then
  printh("CONFVAL "..l.."="..tostr(v,true))
 else
  printh("CONFVAL "..l.."="..tostr(v))
 end
end
function _s(l) _v(l,_sh()) end
"""

CARTS = {}

